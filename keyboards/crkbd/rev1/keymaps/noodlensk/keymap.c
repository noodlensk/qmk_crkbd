#define SHIFT_PRESSED 0x01
#define SHIFT_RELEASED 0x00

void hid_send_current_layer(uint16_t layer, bool shift_pressed) {
    uint8_t packet[32];

    packet[0] = APP_EVENT_MODE_CHANGE;
    packet[1] = layer;
    packet[2] = shift_pressed ? SHIFT_PRESSED : SHIFT_RELEASED;

    raw_hid_send(packet, sizeof(packet));
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    static bool shift_pressed = false;

    // Handle Shift key events
    if (keycode == KC_LSFT || keycode == KC_RSFT) {
        shift_pressed = record->event.pressed;
        hid_send_current_layer(APP_EVENT_MODE_CHANGE_BASE, shift_pressed);
    }

    // Handle layer changes and update shift status
    if (keycode == MO(1) || keycode == MO(2)) {
        if (!record->event.pressed) {
            hid_send_current_layer(APP_EVENT_MODE_CHANGE_BASE, shift_pressed);
        } else if (keycode == MO(1)) {
            hid_send_current_layer(APP_EVENT_MODE_CHANGE_LOWER, shift_pressed);
        } else if (keycode == MO(2)) {
            hid_send_current_layer(APP_EVENT_MODE_CHANGE_UPPER, shift_pressed);
        }
    }

    return true;
}
